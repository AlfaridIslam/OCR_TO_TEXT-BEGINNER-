    "from flask import Flask, request\n",
    "from pytesseract import image_to_string\n",
    "\n",
    "app = Flask(__name__)\n",
    "\n",
    "@app.route('/ocr_to_text', methods=['POST'])\n",
    "def ocr_to_text():\n",
    "    files = request.files.getlist('file')\n",
    "    if len(files) > 5:\n",
    "        return 'Error: Maximum number of pages exceeded (5 pages limit)', 400\n",
    "\n",
    "    text = \"\"\n",
    "    for file in files:\n",
    "        image = file.read()\n",
    "        text += image_to_string(image)\n",
    "    \n",
    "    return text\n",
    "\n",
    "if __name__ == '__main__':\n",
    "    app.run(debug=True)\n"
